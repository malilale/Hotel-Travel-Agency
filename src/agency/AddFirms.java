    private void selectFirmBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selectFirmBoxActionPerformed
        
        if(isTicket){
            if(selectFirmBox.getSelectedIndex() == 0)
                return;
            String fName = (String) selectFirmBox.getSelectedItem();
            Float point = op.getPoint(fName);
            pointToUpdate.setText(Float.toString(point));
        }else{
            String key = (String) selectFirmBox.getSelectedItem();
            points = hotelMap.get(key);
            pointToUpdate.setText(Float.toString(points));
        }
    }//GEN-LAST:event_selectFirmBoxActionPerformed

    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
        if(selectFirmBox.getSelectedIndex()==0)
            return;
        
        if(isTicket){
            String fName = (String) selectFirmBox.getSelectedItem();
            float point = Float.parseFloat(pointToUpdate.getText());
            op.updateFirmPoints(fName,point);
        }else{
            String fName = (String) selectFirmBox.getSelectedItem();
            float point = Float.parseFloat(pointToUpdate.getText());
            op.updateHotelPoints(fName,point);
        }
    }//GEN-LAST:event_btn_updateActionPerformed

    private void btn_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addActionPerformed
        if(firmName.getText().isEmpty() || firmName.getText().matches("Firma Adı") || pointsToAdd.getText().isEmpty())
            return;
        String fName = firmName.getText();
        float point = Float.parseFloat(pointsToAdd.getText());
        
        op.addFirm(fName,point);
    }//GEN-LAST:event_btn_addActionPerformed

