    private void btn_hotelsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hotelsActionPerformed
        btn_firms.setBackground(Color.gray);
        btn_hotels.setBackground(actColor);
        isTicket = false;
        
        hotelMap = op.getHotels();
        String[] hotels = hotelMap.keySet().toArray(new String[0]);
        selectFirmBox.setModel(new javax.swing.DefaultComboBoxModel<>(hotels));
        
        String key = (String) selectFirmBox.getItemAt(0);
        points = hotelMap.get(key);
        pointToUpdate.setText(Float.toString(points));
        
        jLabel2.setVisible(false);
        jLabel3.setVisible(false);
        firmName.setVisible(false);
        pointsToAdd.setVisible(false);
        btn_add.setVisible(false);
    }//GEN-LAST:event_btn_hotelsActionPerformed

